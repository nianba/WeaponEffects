	private static WeaponSlashProfile Profile(
		SlashProfileId id,
		Color slashColor,
		int swingDustType,
		Color swingColor,
		Color swingAlternateColor,
		int swingCount,
		float swingMinScale,
		float swingMaxScale,
		float swingVelocityScale,
		float swingSpreadRadians,
		int hitDustType,
		Color hitColor,
		Color hitAlternateColor,
		int hitCount,
		float hitMinScale,
		float hitMaxScale,
		float hitVelocityScale,
		float hitSpreadRadians,
		float lengthScale,
		float thicknessScale,
		bool swingNoGravity = true,
		bool hitNoGravity = true,
		SlashParticleVisualStyle particleVisualStyle = SlashParticleVisualStyle.Dust)
	{
		return new WeaponSlashProfile(
			id,
			slashColor,
			new SlashParticleProfile(
				swingDustType,
				swingColor,
				swingCount,
				swingMinScale,
				swingMaxScale,
				swingVelocityScale,
				swingSpreadRadians,
				swingAlternateColor,
				swingNoGravity,
				visualStyle: particleVisualStyle),
			new SlashParticleProfile(
				hitDustType,
				hitColor,
				hitCount,
				hitMinScale,
				hitMaxScale,
				hitVelocityScale,
				hitSpreadRadians,
				hitAlternateColor,
				hitNoGravity,
				visualStyle: particleVisualStyle),
			new SlashShapeProfile(
				lengthScale,
				thicknessScale,
				minYScale: 0.45f,
				maxYScale: 0.65f,
				angleRandomness: 0.3f,
				extraUpdates: 5));
	}
